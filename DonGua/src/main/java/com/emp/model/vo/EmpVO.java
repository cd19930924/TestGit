package com.emp.model.vo;

public class EmpVO {
	private Integer empId;
	private String empAcct; 
	private String empPosition;
	private String empPwd;
	private String empName;
	private String empPhone;
	private String empStatus;
	private String empAuthNo;
	private String empAuthName;
	
	public Integer getEmpId() {
		return empId;
	}
	public void setEmpId(Integer empId) {
		this.empId = empId;
	}
	public String getEmpAcct() {
		return empAcct;
	}
	public void setEmpAcct(String empAcct) {
		this.empAcct = empAcct;
	}
	public String getEmpPosition() {
		return empPosition;
	}
	public void setEmpPosition(String empPosition) {
		this.empPosition = empPosition;
	}
	public String getEmpPwd() {
		return empPwd;
	}
	public void setEmpPwd(String empPwd) {
		this.empPwd = empPwd;
	}
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	public String getEmpPhone() {
		return empPhone;
	}
	public void setEmpPhone(String empPhone) {
		this.empPhone = empPhone;
	}
	public String getEmpStatus() {
		return empStatus;
	}
	public void setEmpStatus(String empStatus) {
		this.empStatus = empStatus;
	}
	public String getEmpAuthNo() {
		return empAuthNo;
	}
	public void setEmpAuthNo(String empAuthNo) {
		this.empAuthNo = empAuthNo;
	}
	public String getEmpAuthName() {
		return empAuthName;
	}
	public void setEmpAuthName(String empAuthName) {
		this.empAuthName = empAuthName;
	}
}	
